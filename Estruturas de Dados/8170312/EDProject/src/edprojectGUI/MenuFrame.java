    private void pesquisarReachUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarReachUserButtonActionPerformed
        //Declaração das variáveis de auxilio
        int id_;
        Iterator itr;
        String x = "<html>";
        User user;

        //Input de ID do User para pesquisa de Users alcancaveis
        try {
            id_ = Integer.parseInt(idReachUserTextField.getText());
            itr = social.reachableUsers(social.getVertex(id_));

            //Imprimir no jLabel os users alcancaveis
            while (itr.hasNext()) {
                user = (User) itr.next();
                x = x + " ID : " + user.getId() + " Nome : " + user.getNome() + " Idade : " + user.getIdade() + " E-Mail : " + user.getEmail() + "<br/>";
            }
            x = x + "</html>";
            //Set text do jLabel
            reachUserLabel.setText(x);
        } catch (Exception e) {
            System.out.println("ERRO - Não foi inserido um ID válido!");
        }
    }//GEN-LAST:event_pesquisarReachUserButtonActionPerformed

    private void idFinderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idFinderButtonActionPerformed
        //Declara variáveis
        int id = Integer.parseInt(idFinderTextField.getText());
        User user = social.getVertex(id);

        //Imprime nos jLabel as informações do User
        findUserNomeLabel.setText(user.getNome());
        findUserIdadeLabel.setText("" + user.getIdade());
        findUserEmailLabel.setText("" + user.getEmail());
        findUserViewLabel.setText("" + user.getVisualizacoes());

        String output = "<html>";
        for (FormacaoAcademica a : user.getFormacao()) {
            output += "Ano : " + a.getAnoFormacao() + "  Formação : " + a.getFormacao() + "<br/>";
        }
        output += "</html>";
        findUserFormacaoLabel.setText(output);
        String output2 = "<html>";

        for (CargosProfissionais a : user.getCargos()) {
            output2 += "Ano : " + a.getAnoCargo() + "  Cargo : " + a.getCargo() + "  Empresa : " + a.getEmpresa() + "<br/>";
        }
        output2 += "</html>";
        findUserCargoLabel.setText(output2);
        String output3 = "<html>";

        for (int i : user.getContacts()) {
            output3 += "ID : " + i + "<br/>";
        }
        output3 += "</html>";
        findUserContactosLabel.setText(output3);
    }//GEN-LAST:event_idFinderButtonActionPerformed

